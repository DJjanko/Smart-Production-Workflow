export function currentLanguage() {
  return localStorage.getItem("spw-language") || JSON.parse(localStorage.getItem("spw-session") || "{}")?.user?.language || "sl";
}

export function label(key) {
  const language = currentLanguage();
  return LABELS[language]?.[key] || LABELS.sl[key] || key;
}

export function statusLabel(value) {
  return label(value);
}
